import Foundation

class Calculater {
    
    func add(number1: Float, number2: Float) -> Float {
        return number1 + number2
        
    }
    
    func subtract(number1: Float, number2: Float) -> Float {
        return number1 - number2
        
    }
    
    func multiply(number1: Float, number2: Float) -> Float {
        return number1 * number2
        
    }
    
    func divide(number1: Float, number2: Float) -> Float {
        return number1 / number2
        
    }
    
    func modules(number1: Int, number2: Int) -> Int {
        return number1 % number2
        
    }
    
}
